// ── Weekly Stats Grid ─────────────────────────────────────────────────────────

class _WeeklyStatsGrid extends StatelessWidget {
  final String checkins;
  final String adherence;
  final String avgPain;
  final String sosAlerts;

  const _WeeklyStatsGrid({
    required this.checkins,
    required this.adherence,
    required this.avgPain,
    required this.sosAlerts,
  });

  @override
  Widget build(BuildContext context) {
    final sosInt = int.tryParse(sosAlerts.split('/').first.trim()) ?? 0;
    final adherencePct =
        int.tryParse(adherence.replaceAll('%', '').trim()) ?? 0;
    final avgPainVal = double.tryParse(avgPain.split('/').first.trim()) ?? 0;

    return Column(
      children: [
        Row(
          children: [
            Expanded(
              child: _StatTile(
                icon: Icons.check_circle_rounded,
                iconColor: AppTheme.accentGreen,
                iconBg: const Color(0xFFDCFCE7),
                label: 'Check-ins',
                value: checkins,
                sublabel: 'out of 7 days',
              ),
            ),
            const SizedBox(width: 12),
            Expanded(
              child: _StatTile(
                icon: Icons.medication_rounded,
                iconColor:
                    adherencePct >= 80
                        ? AppTheme.accentGreen
                        : AppTheme.accentOrange,
                iconBg:
                    adherencePct >= 80
                        ? const Color(0xFFDCFCE7)
                        : const Color(0xFFFFF7ED),
                label: 'Med Adherence',
                value: adherence,
                sublabel: adherencePct >= 80 ? 'On track' : 'Needs attention',
              ),
            ),
          ],
        ),
        const SizedBox(height: 12),
        Row(
          children: [
            Expanded(
              child: _StatTile(
                icon: Icons.healing_rounded,
                iconColor:
                    avgPainVal <= 4
                        ? AppTheme.accentGreen
                        : AppTheme.accentOrange,
                iconBg:
                    avgPainVal <= 4
                        ? const Color(0xFFDCFCE7)
                        : const Color(0xFFFFF7ED),
                label: 'Avg Pain',
                value: avgPain,
                sublabel: avgPainVal <= 4 ? 'Low - stable' : 'Monitor closely',
              ),
            ),
            const SizedBox(width: 12),
            Expanded(
              child: _StatTile(
                icon: Icons.emergency_rounded,
                iconColor:
                    sosInt == 0 ? AppTheme.accentGreen : AppTheme.accentRed,
                iconBg:
                    sosInt == 0
                        ? const Color(0xFFDCFCE7)
                        : const Color(0xFFFFE4E4),
                label: 'SOS Alerts',
                value: sosAlerts,
                sublabel: sosInt == 0 ? 'All clear' : 'Review required',
              ),
            ),
          ],
        ),
      ],
    );
  }
}

class _StatTile extends StatelessWidget {
  final IconData icon;
  final Color iconColor;
  final Color iconBg;
  final String label;
  final String value;
  final String sublabel;

  const _StatTile({
    required this.icon,
