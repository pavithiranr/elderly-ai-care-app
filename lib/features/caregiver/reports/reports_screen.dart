// ── Medication Adherence Card ─────────────────────────────────────────────────

class _MedicationAdherenceCard extends StatelessWidget {
  final List<MedicationComplianceData> meds;
  const _MedicationAdherenceCard({required this.meds});

  double get _overallAdherence {
    if (meds.isEmpty) return 0;
    final total = meds.fold(0, (s, m) => s + m.daysTotal);
    final taken = meds.fold(0, (s, m) => s + m.daysTaken);
    return total > 0 ? taken / total : 0;
  }

  @override
  Widget build(BuildContext context) {
    if (meds.isEmpty) {
      return Container(
        padding: const EdgeInsets.all(20),
        decoration: BoxDecoration(
          color: AppTheme.surfaceWhite,
          borderRadius: BorderRadius.circular(16),
          border: Border.all(color: AppTheme.divider),
        ),
        child: Center(
          child: Text(
            'No medication data available',
            style: GoogleFonts.inter(color: AppTheme.textMid),
          ),
        ),
      );
    }

    final pct = (_overallAdherence * 100).round();
    final overallColor =
        pct >= 85 ? AppTheme.accentGreen : AppTheme.accentOrange;

    return Container(
      padding: const EdgeInsets.all(20),
      decoration: BoxDecoration(
        color: AppTheme.surfaceWhite,
        borderRadius: BorderRadius.circular(16),
        border: Border.all(color: AppTheme.divider),
      ),
      child: Column(
        crossAxisAlignment: CrossAxisAlignment.start,
        children: [
          // Overall percentage header
          Row(
            crossAxisAlignment: CrossAxisAlignment.end,
            children: [
              Text(
                '$pct%',
                style: GoogleFonts.inter(
                  fontSize: 32,
                  fontWeight: FontWeight.w800,
                  color: overallColor,
                  height: 1,
                ),
              ),
              const SizedBox(width: 8),
              Padding(
                padding: const EdgeInsets.only(bottom: 4),
                child: Text(
                  'overall this week',
                  style: GoogleFonts.inter(
                    fontSize: 14,
                    color: AppTheme.textMid,
                    fontWeight: FontWeight.w500,
                  ),
                ),
              ),
            ],
          ),
          const SizedBox(height: 6),
          // Overall bar
          ClipRRect(
            borderRadius: BorderRadius.circular(6),
            child: LinearProgressIndicator(
              value: _overallAdherence,
              backgroundColor: AppTheme.divider,
              valueColor: AlwaysStoppedAnimation<Color>(overallColor),
              minHeight: 8,
            ),
          ),
          const SizedBox(height: 20),

          // Per-medication rows
          const Divider(color: AppTheme.divider, height: 1),
          const SizedBox(height: 16),
          ...meds.map((m) => _MedProgressRow(med: m)),
        ],
      ),
    );
  }
}

class _MedProgressRow extends StatelessWidget {
  final MedicationComplianceData med;
  const _MedProgressRow({required this.med});

  @override
  Widget build(BuildContext context) {
    final pct = med.daysTaken / med.daysTotal;
    final color = pct >= 0.85 ? AppTheme.accentGreen : AppTheme.accentOrange;

    return Padding(
      padding: const EdgeInsets.only(bottom: 16),
      child: Column(
        crossAxisAlignment: CrossAxisAlignment.start,
        children: [
          Row(
            children: [
              Expanded(
                child: Column(
                  crossAxisAlignment: CrossAxisAlignment.start,
                  children: [
                    Text(
                      med.name,
                      style: GoogleFonts.inter(
                        fontSize: 14,
                        fontWeight: FontWeight.w600,
                        color: AppTheme.textDark,
                      ),
                    ),
                    Text(
                      med.time,
                      style: GoogleFonts.inter(
                        fontSize: 12,
                        color: AppTheme.textMid,
                      ),
                    ),
                  ],
                ),
              ),
              Container(
                padding:
                    const EdgeInsets.symmetric(horizontal: 10, vertical: 4),
                decoration: BoxDecoration(
                  color: color.withValues(alpha: 0.1),
                  borderRadius: BorderRadius.circular(20),
                ),
                child: Text(
                  '${med.daysTaken}/${med.daysTotal} days',
                  style: GoogleFonts.inter(
                    fontSize: 13,
                    color: color,
                    fontWeight: FontWeight.w700,
                  ),
                ),
              ),
            ],
          ),
          const SizedBox(height: 8),
          ClipRRect(
            borderRadius: BorderRadius.circular(6),
            child: LinearProgressIndicator(
              value: pct.clamp(0.0, 1.0),
              backgroundColor: AppTheme.divider,
              valueColor: AlwaysStoppedAnimation<Color>(color),
              minHeight: 7,
            ),
          ),
        ],
      ),
    );
  }
}

