/// Daily AI-generated health summary + weekly trend report for caregivers.
/// Uses fl_chart for mood and pain bar charts. MD3 layout, ≥16px fonts.
class ReportsScreen extends StatefulWidget {
  final String? patientId;

  const ReportsScreen({super.key, this.patientId});

  @override
  State<ReportsScreen> createState() => _ReportsScreenState();
}

class _ReportsScreenState extends State<ReportsScreen> {
  static const List<String> _dayLabels = ['M', 'T', 'W', 'T', 'F', 'S', 'S'];

  bool _isGeneratingPDF = false;

  /// Cached patient future - shared by all FutureBuilders so Firestore is
  /// only queried once per screen load.
  late final Future<PatientProfile?> _patientFuture = _loadPatient();

  Future<PatientProfile?> _loadPatient() async {
    // If patientId is provided via route, use it
    if (widget.patientId != null && widget.patientId!.isNotEmpty) {
      return await PatientService.instance.getPatientById(widget.patientId!);
    }

    // Otherwise, get the first patient assigned to this caregiver
    final profile =
        await CaregiverService.instance.getCurrentCaregiverProfile();
    if (profile == null) return null;
    final patients = await PatientService.instance.getPatientsByCaregiver(
      profile.id,
    );
    return patients.isNotEmpty ? patients.first : null;
  }

  static String _getDateRangeLabel() {
    final now = DateTime.now();
    final start = now.subtract(const Duration(days: 6));
    const months = [
      'Jan',
      'Feb',
      'Mar',
      'Apr',
      'May',
      'Jun',
      'Jul',
      'Aug',
      'Sep',
      'Oct',
      'Nov',
      'Dec',
    ];
    if (start.month == now.month) {
      return '${months[start.month - 1]} ${start.day} – ${now.day}, ${now.year}';
    }
    return '${months[start.month - 1]} ${start.day} – ${months[now.month - 1]} ${now.day}, ${now.year}';
  }

  Future<void> _generateAndSharePDF() async {
    if (_isGeneratingPDF) return;
    setState(() => _isGeneratingPDF = true);

    try {
      final patient = await _patientFuture;
      if (patient == null) {
        if (mounted) {
          ScaffoldMessenger.of(context).showSnackBar(
            const SnackBar(content: Text('Error: No patient found')),
          );
        }
        return;
      }

      final patientName = patient.name;
      final stats = await PatientService.instance.getWeeklyStats(patient.id);

      // Generate text report
      final now = DateTime.now();
      final dateStr = 'Mar ${now.day - 6} – Apr ${now.day}, ${now.year}';

      final reportText = '''
📋 Daily Health Summary
$patientName's Health Trends

Period: $dateStr

━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
📊 Today's Summary
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━

✓ Check-ins: ${stats['checkins']}
💊 Medication Adherence: ${stats['adherence']}
🩹 Average Pain: ${stats['avgPain']}
🚨 SOS Alerts: ${stats['sosAlerts']}

━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━

Generated by CareSync AI
${DateTime.now().toString().split('.').first}

This report summarizes the weekly health metrics 
and trends for the patient. Please consult with 
healthcare providers for medical advice.
''';

      // Show in a dialog that can be copied
      if (mounted) {
        showDialog(
          context: context,
          builder:
              (context) => AlertDialog(
                title: const Text('Health Report'),
                content: SingleChildScrollView(
                  child: Text(
                    reportText,
                    style: const TextStyle(fontFamily: 'monospace'),
                  ),
                ),
                actions: [
                  TextButton(
                    onPressed: () => Navigator.pop(context),
                    child: const Text('Close'),
                  ),
                  TextButton(
                    onPressed: () {
                      Clipboard.setData(ClipboardData(text: reportText));
                      Navigator.pop(context);
                      ScaffoldMessenger.of(context).showSnackBar(
                        const SnackBar(
                          content: Text('Report copied to clipboard'),
                        ),
                      );
                    },
                    child: const Text('Copy'),
                  ),
                ],
              ),
        );
      }
    } catch (e) {
      debugPrint('Error generating report: $e');
      if (mounted) {
        ScaffoldMessenger.of(
          context,
        ).showSnackBar(SnackBar(content: Text('Error: ${e.toString()}')));
      }
    } finally {
      if (mounted) {
        setState(() => _isGeneratingPDF = false);
      }
    }
  }

  @override
  Widget build(BuildContext context) {
    return Scaffold(
      appBar: AppBar(
        leading: IconButton(
          icon: const Icon(Icons.arrow_back_rounded),
          onPressed: () => context.pop(),
        ),
        title: Text(
          'Health Report',
          style: GoogleFonts.inter(
            fontSize: 18,
            fontWeight: FontWeight.w700,
            color: Theme.of(context).textTheme.bodyLarge?.color,
          ),
        ),
        actions: [
          TextButton.icon(
            onPressed: _isGeneratingPDF ? null : _generateAndSharePDF,
            icon:
                _isGeneratingPDF
                    ? const SizedBox(
                      width: 16,
                      height: 16,
                      child: CircularProgressIndicator(strokeWidth: 2),
                    )
                    : const Icon(Icons.ios_share_rounded, size: 16),
            label: Text(
              _isGeneratingPDF ? 'Generating...' : 'Share',
              style: GoogleFonts.inter(
                fontSize: 14,
                fontWeight: FontWeight.w600,
              ),
            ),
          ),
          const SizedBox(width: 8),
        ],
        surfaceTintColor: Colors.transparent,
        elevation: 0,
      ),
      body: ListView(
        padding: const EdgeInsets.fromLTRB(20, 20, 20, 40),
        children: [
          // ── Page header ────────────────────────────────────────────────
          Text(
            _getDateRangeLabel(),
            style: GoogleFonts.inter(
              fontSize: 13,
              color: AppTheme.textLight,
              fontWeight: FontWeight.w500,
            ),
          ),
          const SizedBox(height: 4),
          FutureBuilder<PatientProfile?>(
            future: _patientFuture,
            builder: (context, snapshot) {
              final patientName = snapshot.data?.name ?? 'Patient';
              return Text(
                "$patientName's Health Trends",
                style: GoogleFonts.inter(
                  fontSize: 24,
                  fontWeight: FontWeight.bold,
                  color: AppTheme.textDark,
                ),
              );
            },
          ),
          const SizedBox(height: 20),

          // ── AI narrative ───────────────────────────────────────────────
          FutureBuilder<PatientProfile?>(
            future: _patientFuture,
            builder: (context, snapshot) {
              if (snapshot.connectionState == ConnectionState.waiting) {
                return _buildNarrativeShimmer();
              }
              final patient = snapshot.data;
              if (patient == null) {
                return const _AiNarrativeCard(
                  narrative: 'No patient data available.',
                );
              }
              return _AiNarrativeLoader(
                patientId: patient.id,
                patientName: patient.name,
              );
            },
          ),
          const SizedBox(height: 20),

          // ── Weekly stats ───────────────────────────────────────────────
          _SectionHeader(title: 'Weekly Overview'),
          const SizedBox(height: 10),
          FutureBuilder<PatientProfile?>(
            future: _patientFuture,
            builder: (context, patientSnap) {
              if (patientSnap.connectionState == ConnectionState.waiting) {
                return const Center(child: CircularProgressIndicator());
              }
              final patient = patientSnap.data;
              if (patient == null) {
                return const _WeeklyStatsGrid(
                  checkins: '0 / 7',
                  adherence: '0%',
                  avgPain: '0 / 10',
                  sosAlerts: '0',
                );
              }
              return FutureBuilder<Map<String, dynamic>>(
                future: PatientService.instance.getWeeklyStats(patient.id),
                builder: (context, statsSnap) {
                  if (statsSnap.connectionState == ConnectionState.waiting) {
                    return const Center(child: CircularProgressIndicator());
                  }
                  final s = statsSnap.data ?? {};
                  return _WeeklyStatsGrid(
                    checkins: s['checkins'] ?? '0 / 7',
                    adherence: s['adherence'] ?? '0%',
                    avgPain: s['avgPain'] ?? '0 / 10',
                    sosAlerts: s['sosAlerts'] ?? '0',
                  );
                },
              );
            },
          ),
          const SizedBox(height: 20),

          // ── Mood trend chart ───────────────────────────────────────────
          _SectionHeader(title: '7-Day Mood Trend'),
          const SizedBox(height: 4),
          Text(
            'Daily mood score (1 = bad, 5 = great)',
            style: GoogleFonts.inter(fontSize: 13, color: AppTheme.textMid),
          ),
          const SizedBox(height: 10),
          FutureBuilder<PatientProfile?>(
            future: _patientFuture,
            builder: (context, patientSnap) {
              if (patientSnap.connectionState == ConnectionState.waiting) {
                return const Center(child: CircularProgressIndicator());
              }
              final patient = patientSnap.data;
              if (patient == null) {
                return _BarChartCard(
                  data: List.filled(7, 0),
                  dayLabels: _dayLabels,
                  maxY: 5,
                  barColor: AppTheme.accentGreen,
                  gradientColors: [
                    AppTheme.accentGreen,
                    const Color(0xFF4ADE80),
                  ],
                  tooltipSuffix: '/5',
                  yAxisLabel: _moodYLabel,
                );
              }
              return FutureBuilder<Map<String, List<double>>>(
                future: PatientService.instance.getWeeklyMoodPainTrends(
                  patient.id,
                ),
                builder: (context, trendsSnap) {
                  if (trendsSnap.connectionState == ConnectionState.waiting) {
                    return const Center(child: CircularProgressIndicator());
                  }
                  final moodData =
                      trendsSnap.data?['mood'] ?? List.filled(7, 0);
                  return _BarChartCard(
                    data: moodData,
                    dayLabels: _dayLabels,
                    maxY: 5,
                    barColor: AppTheme.accentGreen,
                    gradientColors: [
                      AppTheme.accentGreen,
                      const Color(0xFF4ADE80),
                    ],
                    tooltipSuffix: '/5',
                    yAxisLabel: _moodYLabel,
                  );
                },
              );
            },
          ),
          const SizedBox(height: 20),

          // ── Pain trend chart ───────────────────────────────────────────
          _SectionHeader(title: '7-Day Pain Trend'),
          const SizedBox(height: 4),
          Text(
            'Daily reported pain (0 = none, 10 = severe)',
            style: GoogleFonts.inter(fontSize: 13, color: AppTheme.textMid),
          ),
          const SizedBox(height: 10),
          FutureBuilder<PatientProfile?>(
            future: _patientFuture,
            builder: (context, patientSnap) {
              if (patientSnap.connectionState == ConnectionState.waiting) {
                return const Center(child: CircularProgressIndicator());
              }
              final patient = patientSnap.data;
              if (patient == null) {
                return _BarChartCard(
                  data: List.filled(7, 0),
                  dayLabels: _dayLabels,
                  maxY: 10,
                  barColor: AppTheme.accentOrange,
                  gradientColors: [
                    AppTheme.accentOrange,
                    const Color(0xFFFB923C),
                  ],
                  tooltipSuffix: '/10',
                  yAxisLabel: _painYLabel,
                );
              }
              return FutureBuilder<Map<String, List<double>>>(
                future: PatientService.instance.getWeeklyMoodPainTrends(
                  patient.id,
                ),
                builder: (context, trendsSnap) {
                  if (trendsSnap.connectionState == ConnectionState.waiting) {
                    return const Center(child: CircularProgressIndicator());
                  }
                  final painData =
                      trendsSnap.data?['pain'] ?? List.filled(7, 0);
                  return _BarChartCard(
                    data: painData,
                    dayLabels: _dayLabels,
                    maxY: 10,
                    barColor: AppTheme.accentOrange,
                    gradientColors: [
                      AppTheme.accentOrange,
                      const Color(0xFFFB923C),
                    ],
                    tooltipSuffix: '/10',
                    yAxisLabel: _painYLabel,
                  );
                },
              );
            },
          ),
          const SizedBox(height: 20),

          // ── Medication adherence ───────────────────────────────────────
          _SectionHeader(title: 'Medication Adherence'),
          const SizedBox(height: 10),
          FutureBuilder<PatientProfile?>(
            future: _patientFuture,
            builder: (context, patientSnap) {
              if (patientSnap.connectionState == ConnectionState.waiting) {
                return const Center(child: CircularProgressIndicator());
              }
              final patient = patientSnap.data;
              if (patient == null) return const SizedBox.shrink();
              return FutureBuilder<List<MedicationComplianceData>>(
                future: PatientService.instance.getWeeklyMedicationCompliance(
                  patient.id,
                ),
                builder: (context, medSnap) {
                  return _MedicationAdherenceCard(meds: medSnap.data ?? []);
                },
              );
            },
          ),
          const SizedBox(height: 20),
        ],
      ),
    );
  }

  Widget _buildNarrativeShimmer() {
    return Container(
      height: 160,
      decoration: BoxDecoration(
        gradient: const LinearGradient(
          colors: [Color(0xFF1D4ED8), Color(0xFF4338CA)],
          begin: Alignment.topLeft,
          end: Alignment.bottomRight,
        ),
        borderRadius: BorderRadius.circular(20),
      ),
      child: const Center(
        child: CircularProgressIndicator(color: Colors.white, strokeWidth: 2),
      ),
    );
  }

  static String _moodYLabel(double value) {
    return switch (value.toInt()) {
      1 => '😢',
      2 => '😟',
      3 => '😐',
      4 => '🙂',
      5 => '😊',
      _ => '',
    };
  }

  static String _painYLabel(double value) {
    if (value % 2 != 0) return '';
    return value.toInt().toString();
  }
}

// ── AI Narrative Loader ───────────────────────────────────────────────────────

class _AiNarrativeLoader extends StatefulWidget {
  final String patientId;
  final String patientName;
  const _AiNarrativeLoader({
    required this.patientId,
    required this.patientName,
  });

  @override
  State<_AiNarrativeLoader> createState() => _AiNarrativeLoaderState();
}

class _AiNarrativeLoaderState extends State<_AiNarrativeLoader> {
  String? _narrative;
  bool _isLoading = true;
  String? _lastPatientId;

  @override
  void initState() {
    super.initState();
    _lastPatientId = widget.patientId;
    _fetchNarrative();
  }

  @override
  void didUpdateWidget(_AiNarrativeLoader old) {
    super.didUpdateWidget(old);
    if (old.patientId != widget.patientId) {
      _lastPatientId = widget.patientId;
      _narrative = null;
      _fetchNarrative();
    }
  }

  Future<void> _fetchNarrative() async {
    if (!mounted) return;
    setState(() => _isLoading = true);

    final patientId = widget.patientId;
    final patientName = widget.patientName;

    // Step 1: fetch health data - kept outside try so fallback can use it
    PatientHealthData? health;
    int sosCount = 0;
    try {
      final results = await Future.wait([
        PatientService.instance.getTodayHealthData(patientId),
        PatientService.instance.getTodaySosCount(patientId),
      ]);
      if (!mounted || _lastPatientId != patientId) return;
      health = results[0] as PatientHealthData?;
      sosCount = results[1] as int;
    } catch (_) {
      if (mounted && _lastPatientId == patientId) {
        setState(() {
          _narrative =
              '$patientName\'s health data could not be loaded right now.';
          _isLoading = false;
        });
      }
      return;
    }

    final events = [
      'Patient: $patientName',
      if (health != null) ...[
        'Mood today: ${health.mood}',
        'Pain level today: ${health.painLevel} / 10',
        'Medications taken: ${health.medicationsTaken} of ${health.medicationsTotal}',
        if (sosCount > 0) 'SOS alerts today: $sosCount',
      ] else
        'No check-in recorded yet today',
    ];

    // Step 2: call Gemini - fall back to real data on any failure
    try {
      final narrative = await GeminiService.instance.generateDailySummary(
        patientName: patientName,
        events: events,
      );
      if (mounted && _lastPatientId == patientId) {
        setState(() {
          _narrative = narrative;
          _isLoading = false;
        });
      }
    } catch (_) {
      if (mounted && _lastPatientId == patientId) {
        setState(() {
          _narrative = _buildFallback(patientName, health, sosCount);
          _isLoading = false;
        });
      }
    }
  }

  String _buildFallback(String name, PatientHealthData? health, int sosCount) {
    if (health == null) {
      return '$name has not completed today\'s check-in yet. Remind them to log their mood and pain level.';
    }
    final pain = health.painLevel.toInt();
    final painDesc =
        pain <= 3
            ? 'low pain ($pain/10)'
            : pain <= 6
            ? 'moderate pain ($pain/10)'
            : 'high pain ($pain/10)';
    final meds =
        health.medicationsTotal > 0
            ? ' ${health.medicationsTaken}/${health.medicationsTotal} medications taken.'
            : '';
    final sos =
        sosCount > 0
            ? ' ⚠️ $sosCount SOS alert${sosCount > 1 ? 's' : ''} triggered today.'
            : '';
    return '$name checked in today feeling ${health.mood} with $painDesc.$meds$sos';
  }

  @override
  Widget build(BuildContext context) {
    if (_isLoading) {
      return Container(
        height: 160,
        decoration: BoxDecoration(
          gradient: const LinearGradient(
            colors: [Color(0xFF1D4ED8), Color(0xFF4338CA)],
            begin: Alignment.topLeft,
            end: Alignment.bottomRight,
          ),
          borderRadius: BorderRadius.circular(20),
        ),
        child: const Center(
          child: CircularProgressIndicator(color: Colors.white, strokeWidth: 2),
        ),
      );
    }
    return _AiNarrativeCard(narrative: _narrative!);
  }
}

