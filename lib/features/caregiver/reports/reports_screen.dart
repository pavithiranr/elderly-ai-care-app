// ── Bar Chart Card ────────────────────────────────────────────────────────────

class _BarChartCard extends StatefulWidget {
  final List<double> data;
  final List<String> dayLabels;
  final double maxY;
  final Color barColor;
  final List<Color> gradientColors;
  final String tooltipSuffix;
  final String Function(double) yAxisLabel;

  const _BarChartCard({
    required this.data,
    required this.dayLabels,
    required this.maxY,
    required this.barColor,
    required this.gradientColors,
    required this.tooltipSuffix,
    required this.yAxisLabel,
  });

  @override
  State<_BarChartCard> createState() => _BarChartCardState();
}

class _BarChartCardState extends State<_BarChartCard> {
  int _touchedIndex = -1;

  @override
  Widget build(BuildContext context) {
    return Container(
      padding: const EdgeInsets.fromLTRB(16, 20, 16, 12),
      decoration: BoxDecoration(
        color: AppTheme.surfaceWhite,
        borderRadius: BorderRadius.circular(16),
        border: Border.all(color: AppTheme.divider),
      ),
      child: SizedBox(
        height: 180,
        child: BarChart(
          BarChartData(
            maxY: widget.maxY,
            minY: 0,
            barTouchData: BarTouchData(
              enabled: true,
              touchCallback: (event, response) {
                setState(() {
                  if (event is FlTapUpEvent ||
                      event is FlPointerExitEvent ||
                      event is FlLongPressEnd) {
                    _touchedIndex = -1;
                  } else if (response?.spot != null) {
                    _touchedIndex = response!.spot!.touchedBarGroupIndex;
                  }
                });
              },
              touchTooltipData: BarTouchTooltipData(
                getTooltipColor: (_) => AppTheme.textDark,
                tooltipRoundedRadius: 8,
                getTooltipItem: (group, groupIndex, rod, rodIndex) {
                  return BarTooltipItem(
                    '${rod.toY.toStringAsFixed(0)}${widget.tooltipSuffix}',
                    GoogleFonts.inter(
                      color: Colors.white,
                      fontSize: 13,
                      fontWeight: FontWeight.w600,
                    ),
                  );
                },
              ),
            ),
            titlesData: FlTitlesData(
              show: true,
              bottomTitles: AxisTitles(
                sideTitles: SideTitles(
                  showTitles: true,
                  reservedSize: 28,
                  getTitlesWidget: (value, meta) {
                    final i = value.toInt();
                    if (i < 0 || i >= widget.dayLabels.length) {
                      return const SizedBox.shrink();
                    }
                    final isToday = i == 3; // Thu = today (Apr 3)
                    return Padding(
                      padding: const EdgeInsets.only(top: 8),
                      child: Text(
                        widget.dayLabels[i],
                        style: GoogleFonts.inter(
                          fontSize: 13,
                          fontWeight:
                              isToday ? FontWeight.w700 : FontWeight.w400,
                          color: isToday
                              ? AppTheme.primaryBlue
                              : AppTheme.textMid,
                        ),
                      ),
                    );
                  },
                ),
              ),
              leftTitles: AxisTitles(
                sideTitles: SideTitles(
                  showTitles: true,
                  reservedSize: 32,
                  interval: widget.maxY <= 5 ? 1 : 2,
                  getTitlesWidget: (value, meta) {
                    final label = widget.yAxisLabel(value);
                    if (label.isEmpty) return const SizedBox.shrink();
                    return Padding(
                      padding: const EdgeInsets.only(right: 4),
                      child: Text(
                        label,
                        style: GoogleFonts.inter(
                          fontSize: 11,
                          color: AppTheme.textLight,
                        ),
                        textAlign: TextAlign.right,
                      ),
                    );
                  },
                ),
              ),
              topTitles:
                  const AxisTitles(sideTitles: SideTitles(showTitles: false)),
              rightTitles:
                  const AxisTitles(sideTitles: SideTitles(showTitles: false)),
            ),
            gridData: FlGridData(
              show: true,
              drawVerticalLine: false,
              horizontalInterval: widget.maxY <= 5 ? 1 : 2,
              getDrawingHorizontalLine: (_) => const FlLine(
                color: AppTheme.divider,
                strokeWidth: 1,
                dashArray: [4, 4],
              ),
            ),
            borderData: FlBorderData(show: false),
            barGroups: widget.data.asMap().entries.map((entry) {
              final i = entry.key;
              final value = entry.value;
              final isTouched = i == _touchedIndex;

              return BarChartGroupData(
                x: i,
                barRods: [
                  BarChartRodData(
                    toY: value,
                    width: isTouched ? 22 : 18,
                    borderRadius: const BorderRadius.vertical(
                      top: Radius.circular(6),
                    ),
                    gradient: LinearGradient(
                      colors: isTouched
                          ? [
                              widget.gradientColors[1],
                              widget.gradientColors[0],
                            ]
                          : [
