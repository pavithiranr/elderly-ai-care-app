// ── Bar Chart Card ────────────────────────────────────────────────────────────

class _BarChartCard extends StatefulWidget {
  final List<double> data;
  final List<String> dayLabels;
  final double maxY;
  final Color barColor;
  final List<Color> gradientColors;
  final String tooltipSuffix;
  final String Function(double) yAxisLabel;

  const _BarChartCard({
    required this.data,
    required this.dayLabels,
    required this.maxY,
    required this.barColor,
    required this.gradientColors,
    required this.tooltipSuffix,
    required this.yAxisLabel,
  });

  @override
  State<_BarChartCard> createState() => _BarChartCardState();
}

class _BarChartCardState extends State<_BarChartCard> {
  int _touchedIndex = -1;

  @override
  Widget build(BuildContext context) {
    final isDarkMode = Theme.of(context).brightness == Brightness.dark;
    return Container(
      padding: const EdgeInsets.fromLTRB(16, 20, 16, 12),
      decoration: BoxDecoration(
        color:
            isDarkMode
                ? Theme.of(context).colorScheme.surface
                : AppTheme.surfaceWhite,
        borderRadius: BorderRadius.circular(16),
        border: Border.all(
          color:
              isDarkMode
                  ? Colors.white.withValues(alpha: 0.1)
                  : AppTheme.divider,
        ),
        boxShadow:
            isDarkMode
                ? [
                  BoxShadow(
                    color: Colors.black.withValues(alpha: 0.3),
                    blurRadius: 8,
                    offset: const Offset(0, 2),
                  ),
                ]
                : [],
      ),
      child: SizedBox(
        height: 180,
        child: BarChart(
          BarChartData(
            maxY: widget.maxY,
            minY: 0,
            barTouchData: BarTouchData(
              enabled: true,
              touchCallback: (event, response) {
                setState(() {
                  if (event is FlTapUpEvent ||
                      event is FlPointerExitEvent ||
                      event is FlLongPressEnd) {
                    _touchedIndex = -1;
                  } else if (response?.spot != null) {
                    _touchedIndex = response!.spot!.touchedBarGroupIndex;
                  }
                });
              },
              touchTooltipData: BarTouchTooltipData(
                getTooltipColor:
                    (_) =>
                        isDarkMode
                            ? Colors.white.withValues(alpha: 0.87)
                            : AppTheme.textDark,
                tooltipRoundedRadius: 8,
                getTooltipItem: (group, groupIndex, rod, rodIndex) {
                  return BarTooltipItem(
                    '${rod.toY.toStringAsFixed(0)}${widget.tooltipSuffix}',
                    GoogleFonts.inter(
                      color: isDarkMode ? Colors.black : Colors.white,
                      fontSize: 13,
                      fontWeight: FontWeight.w600,
                    ),
                  );
                },
              ),
            ),
            titlesData: FlTitlesData(
              show: true,
              bottomTitles: AxisTitles(
                sideTitles: SideTitles(
                  showTitles: true,
                  reservedSize: 28,
                  getTitlesWidget: (value, meta) {
                    final i = value.toInt();
                    if (i < 0 || i >= widget.dayLabels.length) {
                      return const SizedBox.shrink();
                    }
                    final isToday = i == 3; // Thu = today (Apr 3)
                    return Padding(
                      padding: const EdgeInsets.only(top: 8),
                      child: Text(
                        widget.dayLabels[i],
                        style: GoogleFonts.inter(
                          fontSize: 13,
                          fontWeight:
                              isToday ? FontWeight.w700 : FontWeight.w400,
                          color:
                              isToday ? AppTheme.primaryBlue : AppTheme.textMid,
                        ),
                      ),
                    );
                  },
                ),
              ),
              leftTitles: AxisTitles(
                sideTitles: SideTitles(
                  showTitles: true,
                  reservedSize: 32,
                  interval: widget.maxY <= 5 ? 1 : 2,
                  getTitlesWidget: (value, meta) {
                    final label = widget.yAxisLabel(value);
                    if (label.isEmpty) return const SizedBox.shrink();
                    return Padding(
                      padding: const EdgeInsets.only(right: 4),
                      child: Text(
                        label,
                        style: GoogleFonts.inter(
                          fontSize: 11,
                          color: AppTheme.textLight,
                        ),
                        textAlign: TextAlign.right,
                      ),
                    );
                  },
                ),
              ),
              topTitles: const AxisTitles(
                sideTitles: SideTitles(showTitles: false),
              ),
              rightTitles: const AxisTitles(
                sideTitles: SideTitles(showTitles: false),
              ),
            ),
            gridData: FlGridData(
              show: true,
              drawVerticalLine: false,
              horizontalInterval: widget.maxY <= 5 ? 1 : 2,
              getDrawingHorizontalLine:
                  (_) => const FlLine(
                    color: AppTheme.divider,
                    strokeWidth: 1,
                    dashArray: [4, 4],
                  ),
            ),
            borderData: FlBorderData(show: false),
            barGroups:
                widget.data.asMap().entries.map((entry) {
                  final i = entry.key;
                  final value = entry.value;
                  final isTouched = i == _touchedIndex;

                  return BarChartGroupData(
                    x: i,
                    barRods: [
                      BarChartRodData(
                        toY: value,
                        width: isTouched ? 22 : 18,
                        borderRadius: const BorderRadius.vertical(
                          top: Radius.circular(6),
                        ),
                        gradient: LinearGradient(
                          colors:
                              isTouched
                                  ? [
                                    widget.gradientColors[1],
                                    widget.gradientColors[0],
                                  ]
                                  : [
                                    widget.gradientColors[0].withValues(
                                      alpha: 0.7,
                                    ),
                                    widget.gradientColors[1],
                                  ],
                          begin: Alignment.bottomCenter,
                          end: Alignment.topCenter,
                        ),
                        backDrawRodData: BackgroundBarChartRodData(
                          show: true,
                          toY: widget.maxY,
                          color: AppTheme.backgroundGray,
                        ),
                      ),
                    ],
                  );
                }).toList(),
          ),
        ),
      ),
    );
  }
}

// ── Medication Adherence Card ─────────────────────────────────────────────────

class _MedicationAdherenceCard extends StatelessWidget {
  final List<MedicationComplianceData> meds;
  const _MedicationAdherenceCard({required this.meds});

  double get _overallAdherence {
    if (meds.isEmpty) return 0;
    final total = meds.fold(0, (s, m) => s + m.daysTotal);
    final taken = meds.fold(0, (s, m) => s + m.daysTaken);
    return total > 0 ? taken / total : 0;
  }

  @override
  Widget build(BuildContext context) {
    if (meds.isEmpty) {
      return Container(
        padding: const EdgeInsets.all(20),
        decoration: BoxDecoration(
          color: AppTheme.surfaceWhite,
          borderRadius: BorderRadius.circular(16),
          border: Border.all(color: AppTheme.divider),
        ),
        child: Center(
          child: Text(
            'No medication data available',
            style: GoogleFonts.inter(color: AppTheme.textMid),
          ),
        ),
      );
    }

    final pct = (_overallAdherence * 100).round();
    final overallColor =
        pct >= 85 ? AppTheme.accentGreen : AppTheme.accentOrange;

    return Container(
      padding: const EdgeInsets.all(20),
      decoration: BoxDecoration(
        color: AppTheme.surfaceWhite,
        borderRadius: BorderRadius.circular(16),
        border: Border.all(color: AppTheme.divider),
      ),
      child: Column(
        crossAxisAlignment: CrossAxisAlignment.start,
        children: [
          // Overall percentage header
          Row(
            crossAxisAlignment: CrossAxisAlignment.end,
            children: [
              Text(
                '$pct%',
                style: GoogleFonts.inter(
                  fontSize: 32,
                  fontWeight: FontWeight.w800,
                  color: overallColor,
                  height: 1,
                ),
              ),
              const SizedBox(width: 8),
              Padding(
                padding: const EdgeInsets.only(bottom: 4),
                child: Text(
                  'overall this week',
                  style: GoogleFonts.inter(
                    fontSize: 14,
                    color: AppTheme.textMid,
                    fontWeight: FontWeight.w500,
                  ),
                ),
              ),
            ],
          ),
          const SizedBox(height: 6),
          // Overall bar
          ClipRRect(
            borderRadius: BorderRadius.circular(6),
            child: LinearProgressIndicator(
              value: _overallAdherence,
              backgroundColor: AppTheme.divider,
              valueColor: AlwaysStoppedAnimation<Color>(overallColor),
              minHeight: 8,
            ),
          ),
          const SizedBox(height: 20),

          // Per-medication rows
          const Divider(color: AppTheme.divider, height: 1),
          const SizedBox(height: 16),
          ...meds.map((m) => _MedProgressRow(med: m)),
        ],
      ),
    );
  }
}

class _MedProgressRow extends StatelessWidget {
  final MedicationComplianceData med;
  const _MedProgressRow({required this.med});

  @override
  Widget build(BuildContext context) {
    final pct = med.daysTaken / med.daysTotal;
    final color = pct >= 0.85 ? AppTheme.accentGreen : AppTheme.accentOrange;

    return Padding(
      padding: const EdgeInsets.only(bottom: 16),
      child: Column(
        crossAxisAlignment: CrossAxisAlignment.start,
        children: [
          Row(
            children: [
              Expanded(
                child: Column(
                  crossAxisAlignment: CrossAxisAlignment.start,
                  children: [
                    Text(
                      med.name,
                      style: GoogleFonts.inter(
                        fontSize: 14,
                        fontWeight: FontWeight.w600,
                        color: AppTheme.textDark,
                      ),
                    ),
                    Text(
                      med.time,
                      style: GoogleFonts.inter(
                        fontSize: 12,
                        color: AppTheme.textMid,
                      ),
                    ),
                  ],
                ),
              ),
              Container(
                padding: const EdgeInsets.symmetric(
                  horizontal: 10,
                  vertical: 4,
                ),
                decoration: BoxDecoration(
                  color: color.withValues(alpha: 0.1),
                  borderRadius: BorderRadius.circular(20),
                ),
                child: Text(
                  '${med.daysTaken}/${med.daysTotal} days',
                  style: GoogleFonts.inter(
                    fontSize: 13,
                    color: color,
                    fontWeight: FontWeight.w700,
                  ),
                ),
              ),
            ],
          ),
          const SizedBox(height: 8),
          ClipRRect(
            borderRadius: BorderRadius.circular(6),
            child: LinearProgressIndicator(
              value: pct.clamp(0.0, 1.0),
              backgroundColor: AppTheme.divider,
              valueColor: AlwaysStoppedAnimation<Color>(color),
              minHeight: 7,
            ),
          ),
        ],
      ),
    );
  }
}

